public class SimpleCalculation {
    public static void main(String[] args) {
        
        int a = 5, b = 10;

        System.out.println("Sum: a + b = " + a + " + " + b + " = " + (a + b));
        System.out.println("Difference: a - b = " + a + " - " + b + " = " + (a - b));
        System.out.println("Product: a * b = " + a + " * " + b + " = " + (a * b));
        System.out.println("Quotient: a / b = " + a + " / " + b + " = " + (double)a / b);

    }
}
