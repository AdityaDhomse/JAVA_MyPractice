public class TemperatureConversion {
    public static void main(String[] args) {
        
        double celsius = 25;
        double fahrenheit;

        fahrenheit = (celsius * 9 / 5) + 32;

        System.out.println("Celsius = " + celsius + " \u00B0C");
        System.out.println("Fahrenheit = " + fahrenheit + " \u00B0F");

    }
}
